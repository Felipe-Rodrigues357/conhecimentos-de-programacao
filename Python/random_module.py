from random import *

print(
    round(random(), 2)
) #0.0 - 1.0

print(
    randint(0, 10)
) # Diferença de INT que quiser dentro do parâmetro que foi passado

print(
    round(uniform(1.25, 2.00),2)
) # Número float dentro do parâmetro que foi passado

print(
    randrange(0,100,2)
) # Se coloca o início, final e depois a "diferença" entre eles, nesse caso irão aparecer todos os pares

